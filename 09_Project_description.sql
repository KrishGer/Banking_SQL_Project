# Banking SQL Project

## Project Objective
-Demonstrates real-world banking data analysis using SQL.
-Includes customers, accounts, and transactions tables, with constraints,
-indexes, views, stored procedures, triggers, and analytics queries.

## Tools Used
- MySQL Workbench
- SQL (Advanced Queries, Views, Indexes, Stored Procedures, Triggers)
- Optional: Excel / Power BI for dashboards

## Tables
1. *customers* – Customer details  
2. *accounts* – Accounts linked to customers  
3. *transactions* – Transaction history

## Concepts Used
- CREATE TABLE, INSERT, ALTER TABLE, Constraints  
- Indexes for optimization  
- Views for easier querying  
- Stored Procedures & Triggers  
- SELECT, JOIN, GROUP BY, HAVING  
- Ranking Functions (RANK)  
- Subqueries, String & Date functions

## Sample Queries & Outputs
- Total balance per customer  
- Top 3 accounts by balance  
- Customers with highest account balance  
- First 3 letters of customer names  
- Monthly transaction summary  

